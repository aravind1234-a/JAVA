public class wrapper 
{
    public static void main(String[] args)
    {
        /*primitive         wrapper
         * // int           //Integer
         * // char          //Character
         * // double        //Double
         * // boolean       //Boolean
         */ 
        /* autoboxing: automatic conversion of primitive to reference datatypes*/
        Boolean a=true;
        Character b='@';
        Integer c=123;
        Double d=3.14;
        String e="gfhjd";

    }
}
